package com.sr.dto;

public class RideInfo {
	protected String rideNo;
	protected String cabNo;
	protected String driverName;
	protected String estimatedPickup;
	protected String mobileNo;
	public String getRideNo() {
		return rideNo;
	}
	public void setRideNo(String rideNo) {
		this.rideNo = rideNo;
	}
	public String getCabNo() {
		return cabNo;
	}
	public void setCabNo(String cabNo) {
		this.cabNo = cabNo;
	}
	public String getDriverName() {
		return driverName;
	}
	public void setDriverName(String driverName) {
		this.driverName = driverName;
	}
	public String getEstimatedPickup() {
		return estimatedPickup;
	}
	public void setEstimatedPickup(String estimatedPickup) {
		this.estimatedPickup = estimatedPickup;
	}
	public String getMobileNo() {
		return mobileNo;
	}
	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}
	
}
